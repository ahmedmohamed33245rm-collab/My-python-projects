print("hello to my calculator ")
x=int(input("please enter the first number"))
y=int(input("please enter the second number"))
z=str(input("please enter the operation"))
if z=="/":
    print(x,"/",y,"=",x/y)
elif z=="*":
    print(x,"*",y,"=",x*y)
elif z=="-":
    print(x,"-",y,x-y)
elif z=="+":
    print(x,"+",y,x+y)
else:
    print("looks like something goes wrong")
    